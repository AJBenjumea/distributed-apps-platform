import os

from fabric import Connection
from threading import Thread


class AxonInstaller(object):
    def __init__(self, host, username=None, gw_host=None, gw_user=None):
        self._host = host
        self._username = username
        self._gw_host = gw_host
        self._gw_user = gw_user

    def install_deb_package(self, deb_package_path):
        # File name should end with tar.gz  example:'axon.tar.gz'
        gateway = None
        if self._gw_host:
            gateway = Connection(self._gw_host, user=self._gw_user)
        conn = Connection(self._host, user=self._username, gateway=gateway)
        filename = os.path.basename(deb_package_path)
        conn.put(deb_package_path, '/tmp')
        conn.run('cd /tmp/ && sudo dpkg -i %s' % filename)


